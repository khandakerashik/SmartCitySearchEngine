    private void addButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addButtonActionPerformed
        // TODO add your handling code here:
        try{
            String sql="Insert into Entity (Id,Name,Type,Location,Phone,Email,Website)values (?,?,?,?,?,?,?)";
       pst=conn.prepareStatement(sql);
       pst.setString(1,suggIdField.getText());
       pst.setString(2,nameField.getText());
       pst.setString(3,typeField.getText());
       pst.setString(4,locationField.getText());
       pst.setString(5,phoneField.getText());
       pst.setString(6,emailField.getText());
       pst.setString(7,websiteField.getText());
       pst.execute();
       JOptionPane.showMessageDialog(null, "New Entity has been added for these informations. \nThank You!.");
        }
        catch(Exception e){
            JOptionPane.showMessageDialog(null,e);
        }
        
        String sql="delete from Suggest where Id=?";
        try{
            pst=conn.prepareStatement(sql);
            pst.setString(1,suggIdField.getText());
            pst.execute();
            JOptionPane.showMessageDialog(null,"Suggestion has been deleted!");
        }
        catch(Exception e)
        {
            JOptionPane.showMessageDialog(null,e);
        }
    }//GEN-LAST:event_addButtonActionPerformed

