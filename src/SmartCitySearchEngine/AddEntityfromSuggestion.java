    public void Search(){
        String a1=suggIdField.getText();
        String sql="Select * from Suggest where Id='"+a1+"'";
        try{
            pst=conn.prepareStatement(sql);
            rs=pst.executeQuery();
            if(rs.next())
            {
                nameField.setText(rs.getString(2));
                typeField.setText(rs.getString(3));
                locationField.setText(rs.getString(4));
                phoneField.setText(rs.getString(5));
                emailField.setText(rs.getString(6));
                websiteField.setText(rs.getString(7));
                rs.close();
                pst.close();
            }
            else
            {
                JOptionPane.showMessageDialog(null, "Invalid SUGGESTION ID!!");
            }
            
                    
        }catch(HeadlessException | SQLException e){
            JOptionPane.showMessageDialog(null,e);
        }
    }
